#include "libft.h"

int ft_atoi(const char *str)
{

	int i;
	int j;
	int res;

	i = 0;
	j = 1;
	res = 0;

	while (*str >= '0' && *str <= '9')
	{
		res = (res * 10) + *str - '0';
		str++;
	}
	return (res);

}
